<?php

$i = 1;
while ($i <= 5) {
    echo "The number is " . $i . "<br>" . "\n";
    $i++;
}

/**
 * do...while 语句
 * do...while 语句会至少执行一次代码，然后检查条件，只要条件成立，就会重复进行循环。
 *
 * 语法
 * do
 * {
 * 要执行的代码;
 * }
 * while (条件);
 */
echo "---------------\n";
$j = 10;
do {
    $j++;
    echo "The number is " . $j . "<br>" . "\n";
} while ($j < 5);

